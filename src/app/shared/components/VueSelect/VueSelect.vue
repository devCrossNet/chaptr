<template>
  <div :class="cssClasses">
    <select
      :title="placeholder"
      :aria-labelledby="id"
      :name="name"
      :id="id"
      v-validate="validation"
      :multiple="multiple"
      :required="required"
      :disabled="disabled"
      :autocomplete="autocomplete"
      v-bind="$attrs"
      v-on="handlers"
    >
      <option v-for="(option, idx) in options" :key="idx" :value="option.value" :selected="isSelected(option)">
        {{ option.label }}
      </option>
    </select>
    <i :class="$style.icon" v-if="!multiple" />
  </div>
</template>

<script lang="ts">
import { Validator } from 'vee-validate';

export interface IVueSelectOption {
  label: string;
  value: string;
}

export default {
  name: 'VueSelect',
  inheritAttrs: false,
  inject: {
    $validator: {
      default: new Validator({}, {}),
    },
  },
  props: {
    name: {
      type: String,
      required: true,
    },
    id: {
      type: String,
      required: true,
    },
    options: {
      required: true,
      type: Array,
    },
    value: {
      type: String,
      default: '',
    },
    multiple: {
      type: Boolean,
    },
    required: {
      type: Boolean,
    },
    disabled: {
      type: Boolean,
    },
    validation: {
      type: String,
    },
    autocomplete: {
      type: String,
      default: 'off',
    },
    placeholder: {
      type: String,
    },
  },
  computed: {
    isValid() {
      return this.errors ? this.errors.first(this.name) === null : true;
    },
    cssClasses() {
      const classes = [this.$style.vueSelect];

      if (this.multiple) {
        classes.push(this.$style.multiple);
      }

      if (this.disabled) {
        classes.push(this.$style.disabled);
      }

      if (!this.isValid) {
        classes.push(this.$style.error);
      }

      return classes;
    },
    currentValueAsArray(): string[] {
      return this.value.split('|');
    },
    handlers() {
      delete this.$listeners.input;

      return {
        ...this.$listeners,
        input: this.onInput,
      };
    },
  },
  methods: {
    isSelected(option: IVueSelectOption): boolean {
      return this.currentValueAsArray.indexOf(option.value) > -1;
    },
    onInput(e: Event) {
      const selected: IVueSelectOption[] = [];
      const target: HTMLSelectElement = e.target as HTMLSelectElement;
      const length: number = target.options.length;

      for (let i = 0; i < length; i++) {
        const option: any = target.options[i];
        if (option.selected) {
          selected.push({ label: option.text, value: option.value });
        }
      }

      this.$emit('input', selected.map((option: IVueSelectOption) => option.value).join('|'));
    },
  },
};
</script>

